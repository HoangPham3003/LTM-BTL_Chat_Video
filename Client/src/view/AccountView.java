    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String fullname = txt_fname.getText();
        int sex = Integer.parseInt(buttonGroup1.getSelection().getActionCommand());
        String email = txt_email.getText();
        String address = txt_address.getText();
        
        Account new_acc = new Account(this.current_acc.getUserName(), this.current_acc.getPassWord(), fullname, sex, email, address);
        try {
            int rowCount = iacc.UpdateAccount(new_acc);
            if (rowCount != 0) {
                JOptionPane.showMessageDialog(this, "Update account successfully!");
                txt_usn.setText(new_acc.getUserName());
                txt_fname.setText(new_acc.getFullName());
                txt_email.setText(new_acc.getEmail());
                txt_address.setText(new_acc.getAddress());
                if (new_acc.getSex() == 0) {
                    radio_male.setSelected(true);
                }
                else radio_female.setSelected(true);
                this.current_acc = new_acc;
            }
            else JOptionPane.showMessageDialog(this, "Error!");
        }
        catch(Exception e) {
            System.out.println(e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

