    public AccountView(Account acc) {
        initComponents();
        this.setDefaultCloseOperation(DISPOSE_ON_CLOSE);
        radio_male.setActionCommand("0");
        radio_female.setActionCommand("1");
        
        try {
            this.iacc = (IAccount) Naming.lookup("rmi://localhost:1099/ServerAcc");
        }
        catch(Exception e) {
            System.out.println(e);
        }
        
        try {
            this.current_acc = iacc.FindByUsn(acc.getUserName());
        }
        catch(Exception e) {
            System.out.println(e);
        }    
        txt_usn.setText(this.current_acc.getUserName());
        txt_fname.setText(this.current_acc.getFullName());
        txt_email.setText(this.current_acc.getEmail());
        txt_address.setText(this.current_acc.getAddress());
        if (this.current_acc.getSex() == 0) {
            radio_male.setSelected(true);
        }
        else radio_female.setSelected(true);
    }

